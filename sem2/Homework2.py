# Помогите Кате отгадать задуманные Петей числа
# Инструкция по использованию платформы
# Петя и Катя – брат и сестра. Петя – студент, а Катя – школьница. Петя помогает Кате по математике.
# Он задумывает два натуральных числа X и Y (X,Y≤1000), а Катя должна их отгадать. 
# Для этого Петя делает две подсказки. Он называет сумму этих чисел S и их произведение P. 
# Помогите Кате отгадать задуманные Петей числа.
# Примечание: числа S и P задавать не нужно, они будут передаваться в тестах. 
# В результате вы должны вывести все возможные пары чисел X и Y через пробел, такие что X <= Y.
# Пример
# На входе:
# s = 12
# p = 27
# На выходе:
# 3 9
s = 12
p = 27
def find_numbers(S, P):
    # Проходим по всем возможным значениям X
    for X in range(1, S // 2 + 1):
        Y = S - X  # Вычисляем Y как остаток от суммы
        if X * Y == P:  # Проверяем произведение
            print(X, Y)  # Выводим пару

