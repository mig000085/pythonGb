# """Счастливый билет
# Инструкция по использованию платформы
# Вы пользуетесь общественным транспортом? 
# Вероятно, вы расплачивались за проезд и получали билет с номером.
# Счастливым билетом называют такой билет 
# с шестизначным номером, где сумма первых трех 
# цифр равна сумме последних трех.
# Т.е. билет с номером 385916 – счастливый, т.к. 3+8+5=9+1+6.
# Вам требуется написать программу, которая 
# проверяет счастливость билета с номером n и выводит
# на экран yes или no.
# Пример:
# n = 385916 -> yes
# n = 123456 -> no"""
# """
# При отправке кода на Выполнение раскомментируйте строку ниже, чтобы задать значение `n`

# При отправке решения на Проверку закомментируйте эту строку обратно - система автоматически подставит разные значения `n` для проверки
# """

n = 385916

# Введите ваше решение нижеde 
def is_lucky_ticket(n):
    # Преобразуем номер билета в строку
    ticket_str = str(n)
    
    # Проверяем, что номер билета состоит из шести цифр
    if len(ticket_str) != 6:
        return "no"
    
    # Разделяем билет на две части
    first_half = ticket_str[:3]
    second_half = ticket_str[3:]
    
    # Считаем суммы цифр первой и второй части
    first_sum = sum(int(digit) for digit in first_half)
    second_sum = sum(int(digit) for digit in second_half)
    
    # Проверяем равенство сумм
    if first_sum == second_sum:
        return "yes"
    else:
        return "no"

# Пример использования

result = is_lucky_ticket(n)
print(result)  # Вывод: yes